library(forecast)

# =============================================================
# (α) Χρονοδιάγραμμα σειράς
# =============================================================

plot(nor_ts,
     type = "o", pch = 20, col = "steelblue", lwd = 2,
     main = "Μηνιαίο Ποσοστό Ανεργίας - Νορβηγία (2022-2026)",
     ylab = "Ποσοστό Ανεργίας (%)",
     xlab = "Έτος")
abline(reg = lm(nor_ts ~ time(nor_ts)), col = "darkred", lty = 2, lwd = 1.5)
legend("topleft",
       legend = c("Ανεργία", "Γραμμή Τάσης"),
       col    = c("steelblue", "darkred"),
       lty    = c(1, 2), lwd = 2, bty = "n")
grid()

# =============================================================
# (β) Εξέταση στασιμότητας με ACF και PACF
# =============================================================

par(mfrow = c(1, 2))
acf(nor_ts,  lag.max = 36, main = "ACF Αρχικής Σειράς")
pacf(nor_ts, lag.max = 36, main = "PACF Αρχικής Σειράς")
par(mfrow = c(1, 1))

# Σε περίπτωση στασιμότητας → προσαρμογή ARMA(p,q)
# Η σειρά παρουσιάζει ανοδική τάση και ΔΕΝ είναι στάσιμη
# → προχωράμε στο (γ)
# fit_arma <- auto.arima(nor_ts, d = 0, seasonal = FALSE)
# summary(fit_arma)

# =============================================================
# (γ) Πρώτες διαφορές και εξέταση στασιμότητας
# =============================================================

diff1_ts <- diff(nor_ts, differences = 1)

# Χρονοδιάγραμμα πρώτων διαφορών
plot(diff1_ts,
     type = "o", pch = 20, col = "darkgreen", lwd = 1.5,
     main = "Χρονοσειρά Πρώτων Διαφορών Ανεργίας (d=1)",
     ylab = "Διαφορά Ποσοστού (%)",
     xlab = "Έτος")
abline(h = 0, col = "red", lty = 2, lwd = 2)
grid()

# ACF και PACF πρώτων διαφορών
par(mfrow = c(1, 2))
acf(diff1_ts,  lag.max = 36, main = "ACF Πρώτων Διαφορών (d=1)")
pacf(diff1_ts, lag.max = 36, main = "PACF Πρώτων Διαφορών (d=1)")
par(mfrow = c(1, 1))

# =============================================================
# (ε) Δεύτερες διαφορές (αν οι πρώτες ΔΕΝ είναι στάσιμες)
# =============================================================

diff2_ts <- diff(nor_ts, differences = 2)

# ACF και PACF δεύτερων διαφορών
par(mfrow = c(1, 2))
acf(diff2_ts,  lag.max = 36, main = "ACF Δεύτερων Διαφορών (d=2)")
pacf(diff2_ts, lag.max = 36, main = "PACF Δεύτερων Διαφορών (d=2)")
par(mfrow = c(1, 1))
