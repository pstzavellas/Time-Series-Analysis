library(forecast)

# =============================================================
# (δ) Προσαρμογή ARIMA βάσει πρώτων διαφορών (d=1)
#     (εφόσον οι πρώτες διαφορές είναι στάσιμες)
# =============================================================

fit_arima_d1 <- auto.arima(nor_ts, d = 1, seasonal = FALSE)
summary(fit_arima_d1)

# =============================================================
# (στ) Προσαρμογή ARIMA βάσει δεύτερων διαφορών (d=2)
#      (εφόσον οι πρώτες διαφορές ΔΕΝ είναι στάσιμες)
# =============================================================

fit_arima_d2 <- auto.arima(nor_ts, d = 2, seasonal = FALSE)
summary(fit_arima_d2)

# Σύγκριση AICc μεταξύ των δύο μοντέλων
cat("=== Σύγκριση AICc ===\n")
cat("Μοντέλο (δ) με d=1 - AICc:", fit_arima_d1$aicc, "\n")
cat("Μοντέλο (στ) με d=2 - AICc:", fit_arima_d2$aicc, "\n")
cat("Επιλέγουμε το μοντέλο με το μικρότερο AICc.\n")

# =============================================================
# (ζ) Έλεγχος επάρκειας του επιλεγμένου μοντέλου
# =============================================================

# (Ι) Έλεγχος υπολοίπων
checkresiduals(fit_arima_d1)

# (ΙΙ) Διευρυμένα μοντέλα (Overfitting check)
# Το βέλτιστο μοντέλο είναι ARIMA(0,1,0) → p=0, q=0
# Προσθέτουμε +1 στο p και +1 στο q ξεχωριστά
fit_ext1 <- Arima(nor_ts, order = c(1, 1, 0)) # p: 0 → 1
fit_ext2 <- Arima(nor_ts, order = c(0, 1, 1)) # q: 0 → 1
fit_ext3 <- Arima(nor_ts, order = c(1, 1, 1)) # p και q: 0 → 1

cat("=== Σύγκριση AIC Διευρυμένων Μοντέλων ===\n")
cat("Βέλτιστο μοντέλο ARIMA(0,1,0) AIC:", AIC(fit_arima_d1), "\n")
cat("ARIMA(1,1,0)               AIC:", AIC(fit_ext1), "\n")
cat("ARIMA(0,1,1)               AIC:", AIC(fit_ext2), "\n")
cat("ARIMA(1,1,1)               AIC:", AIC(fit_ext3), "\n")

# =============================================================
# (η) Πρόβλεψη για τα επόμενα 2 χρόνια (24 μήνες)
# =============================================================

forecast_norway <- forecast(fit_arima_d1, h = 24)

plot(forecast_norway,
     main = "Πρόβλεψη Ποσοστού Ανεργίας Νορβηγίας (2026-2028)",
     ylab = "Ποσοστό Ανεργίας (%)",
     xlab = "Έτος",
     col  = "steelblue",
     lwd  = 2)
grid()

# Εκτύπωση τιμών πρόβλεψης
print(forecast_norway)
