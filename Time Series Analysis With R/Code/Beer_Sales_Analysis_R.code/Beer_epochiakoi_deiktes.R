# Χρονοδιάγραμμα αρχικής σειράς
# =============================================================

plot(beer_ts,
     type = "o", pch = 20, col = "steelblue", lwd = 2,
     main = "Τριμηνιαίες Πωλήσεις Μπίρας (2016-2019)",
     ylab = "Πωλήσεις (εκ. μπουκάλια)",
     xlab = "Έτος")
grid()

# =============================================================
# Αποσύνθεση χρονοσειράς (Additive Decomposition)
# =============================================================

beer_decomp <- decompose(beer_ts, type = "additive")
plot(beer_decomp)

# =============================================================
# Εποχιακοί Δείκτες
# =============================================================

# Εξαγωγή εποχιακής συνιστώσας
seasonal_component <- beer_decomp$seasonal

cat("=== Εποχιακοί Δείκτες ===\n")
cat("Χειμώνας:   ", round(seasonal_component[1], 4), "\n")
cat("Άνοιξη:     ", round(seasonal_component[2], 4), "\n")
cat("Καλοκαίρι:  ", round(seasonal_component[3], 4), "\n")
cat("Φθινόπωρο:  ", round(seasonal_component[4], 4), "\n")

# =============================================================
# Εξάλειψη εποχικότητας (Seasonally Adjusted Series)
# =============================================================

beer_adjusted <- beer_ts - seasonal_component

cat("\n=== Απαλλαγμένη από Εποχικότητα Σειρά ===\n")
print(round(beer_adjusted, 4))

# Γράφημα σύγκρισης αρχικής και απαλλαγμένης σειράς
plot(beer_ts,
     type = "o", pch = 20, col = "steelblue", lwd = 2,
     main = "Πωλήσεις Μπίρας: Αρχική vs Απαλλαγμένη από Εποχικότητα",
     ylab = "Πωλήσεις (εκ. μπουκάλια)",
     xlab = "Έτος",
     ylim = range(c(beer_ts, beer_adjusted)))
lines(beer_adjusted,
      type = "o", pch = 20, col = "darkred", lwd = 2, lty = 2)
legend("topleft",
       legend = c("Αρχική Σειρά", "Απαλλαγμένη από Εποχικότητα"),
       col    = c("steelblue", "darkred"),
       lty    = c(1, 2), lwd = 2, bty = "n")
grid()