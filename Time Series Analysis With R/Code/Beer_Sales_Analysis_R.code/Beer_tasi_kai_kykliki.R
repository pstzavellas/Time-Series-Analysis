# =============================================================
# Γραμμή Τάσης (Linear Trend)
# =============================================================

# Δημιουργία μεταβλητής χρόνου
t <- time(beer_ts)

# Προσαρμογή γραμμικού μοντέλου τάσης
trend_model <- lm(beer_ts ~ t)

cat("=== Γραμμή Τάσης ===\n")
summary(trend_model)

# Τιμές τάσης
trend_values <- ts(fitted(trend_model),
                   start     = c(2016, 1),
                   frequency = 4)

# Γράφημα αρχικής σειράς με γραμμή τάσης
plot(beer_ts,
     type = "o", pch = 20, col = "steelblue", lwd = 2,
     main = "Πωλήσεις Μπίρας με Γραμμή Τάσης",
     ylab = "Πωλήσεις (εκ. μπουκάλια)",
     xlab = "Έτος")
lines(trend_values, col = "darkred", lwd = 2, lty = 2)
legend("topleft",
       legend = c("Αρχική Σειρά", "Γραμμή Τάσης"),
       col    = c("steelblue", "darkred"),
       lty    = c(1, 2), lwd = 2, bty = "n")
grid()

# =============================================================
# Κυκλική Μεταβολή (Σχετικά Κυκλικά Κατάλοιπα)
# =============================================================

# Βήμα 1: Υπολογισμός κινητού μέσου (4 περιόδων για τριμηνιαία δεδομένα)
ma_values <- stats::filter(beer_ts,
                           filter = rep(1/4, 4),
                           sides  = 2)

# Βήμα 2: Σχετικά κυκλικά κατάλοιπα = (Αρχική / Κινητός Μέσος) * 100
cyclical <- (beer_ts / ma_values) * 100

cat("\n=== Σχετικά Κυκλικά Κατάλοιπα (%) ===\n")
print(round(cyclical, 2))

# Γράφημα κυκλικής μεταβολής
plot(cyclical,
     type = "o", pch = 20, col = "darkgreen", lwd = 2,
     main = "Κυκλική Μεταβολή Πωλήσεων Μπίρας\n(Σχετικά Κυκλικά Κατάλοιπα)",
     ylab = "Κυκλικό Κατάλοιπο (%)",
     xlab = "Έτος")
abline(h = 100, col = "red", lty = 2, lwd = 1.5)
legend("topleft",
       legend = c("Κυκλικό Κατάλοιπο", "Βάση (100%)"),
       col    = c("darkgreen", "red"),
       lty    = c(1, 2), lwd = 2, bty = "n")
grid()