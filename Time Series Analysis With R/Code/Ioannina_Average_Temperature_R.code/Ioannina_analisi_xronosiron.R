# =============================================================
# (γ) Πρώτες εποχικές διαφορές (lag = 12)
# =============================================================

seasonal_diff_ts <- diff(ioannina_ts, lag = 12)

# Εξέταση στασιμότητας της νέας σειράς με ACF και PACF
par(mfrow = c(1, 2))
acf(seasonal_diff_ts,  lag.max = 36, main = "ACF Εποχικών Διαφορών (D=1)")
pacf(seasonal_diff_ts, lag.max = 36, main = "PACF Εποχικών Διαφορών (D=1)")
par(mfrow = c(1, 1))

# =============================================================
# (δ) Προσαρμογή SARIMA (p,d,q)(P,D,Q)_12 στην αρχική σειρά
#     (εφόσον οι εποχικές διαφορές είναι στάσιμες)
# =============================================================

fit_model <- auto.arima(ioannina_ts, seasonal = TRUE)
summary(fit_model)

# =============================================================
# (ε) Εάν οι εποχικές διαφορές ΔΕΝ είναι στάσιμες:
#     Πρώτες διαφορές επί των εποχικών διαφορών
# =============================================================

double_diff_ts <- diff(seasonal_diff_ts, lag = 1)

# Εξέταση στασιμότητας της νέας σειράς με ACF και PACF
